#include<stdio.h>

int main(){

//   limitations of the Variables 
    

    // // A normal interger variable can take 10 degit of number 
        int a = 1234567891;
        printf("%d", a);

    // //  A long long integer can take 19 degit of number 

        long long int b = 1234567891234567890;
        printf("\n%lld", b); 

    // // NON OF THE float OR double CAN'T GIVE THE CURRECT VALUE  
    // //  A normal float can take 7 degit of floating number 
     
        float c = 1000.424234;
        printf("\n%f", c);

    // //  A double can take 15 degit of floating number 
     
        double d = 3434343.35235242;
        printf("\n%lf", d);

    
    return 0;
}